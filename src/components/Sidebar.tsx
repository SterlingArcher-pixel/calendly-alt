export default function Sidebar() {
  const pathname = usePathname();
  const [host, setHost] = useState<any>(null);
  const { facilities, activeFacilityId, activeFacility, setActiveFacilityId } = useFacility();

  useEffect(() => {
    async function loadHost() {
      const supabase = createClient();
      const { data: { user } } = await supabase.auth.getUser();
      if (!user) return;
      const { data } = await supabase.from("hosts").select("name, email, avatar_url").eq("id", user.id).single();
      setHost(data);
    }
    loadHost();
  }, []);

  const isActive = (href: string) => {
    if (href === "/dashboard") return pathname === "/dashboard";
    return pathname.startsWith(href);
  };

  return (
    <aside className="fixed left-0 top-0 flex h-screen w-[240px] flex-col border-r z-20 bg-[#0B2522]">
      {/* Logo */}
      <div className="px-5 pt-5 pb-3">
        <div className="flex items-center gap-2.5">
          <div className="flex h-8 w-8 items-center justify-center rounded-lg bg-[#00D08A] text-sm font-bold text-[#0B2522]">
            A
          </div>
          <span className="text-[15px] font-semibold text-white">Apploi Scheduling</span>
        </div>
      </div>

      {/* Facility Switcher */}
      {facilities.length > 0 && (
        <div className="px-4 pb-3">
          <select
            value={activeFacilityId || ""}
            onChange={(e) => setActiveFacilityId(e.target.value || null)}
            className="w-full rounded-lg border border-white/10 bg-white/5 px-3 py-2 text-sm font-medium text-white focus:border-[#00D08A] focus:outline-none"
          >
            <option value="">All Facilities</option>
            {facilities.map((f: any) => (
              <option key={f.id} value={f.id}>{f.name}</option>
            ))}
          </select>
          {activeFacility && (
            <p className="mt-1 px-1 text-[11px] text-white/40">
              {activeFacility.city}, {activeFacility.state}
            </p>
          )}
        </div>
      )}

      {/* Nav */}
      <nav className="flex-1 space-y-0.5 px-3 pt-1">
        {navItems.map((item) => {
          const active = isActive(item.href);
          return (
            <Link
              key={item.href}
              href={item.href}
              className={`flex items-center gap-3 rounded-lg px-3 py-2.5 text-sm font-medium transition-colors ${
                active
                  ? "bg-white/10 text-white"
                  : "text-white/60 hover:bg-white/5 hover:text-white/90"
              }`}
            >
              <span className={active ? "text-[#00D08A]" : "text-white/40"}>{item.icon}</span>
              {item.name}
              {active && <div className="ml-auto h-1.5 w-1.5 rounded-full bg-[#00D08A]" />}
            </Link>
          );
        })}
      </nav>

      {/* User */}
      <div className="border-t border-white/10 p-4">
        <div className="flex items-center gap-3">
          {host?.avatar_url ? (
            <img src={host.avatar_url} alt="" className="h-8 w-8 rounded-full" />
          ) : (
            <div className="flex h-8 w-8 items-center justify-center rounded-full bg-[#00D08A]/20 text-xs font-semibold text-[#00D08A]">
              {host?.name?.[0] || "?"}
            </div>
          )}
          <div className="min-w-0 flex-1">
            <p className="truncate text-sm font-medium text-white">{host?.name || "Loading..."}</p>
            <p className="truncate text-[11px] text-white/40">{host?.email}</p>
          </div>
        </div>
      </div>
    </aside>
  );
}
